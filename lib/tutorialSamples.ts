// KSIC 개편(2026-07) 이전 가입자의 구 업종 값 → 현행 업종 매핑
const LEGACY_ALIAS: Record<string, string> = {
    "물류·운수업": "운수 및 창고업",
    "조선·플랜트": "제조업",
    "전기·정보통신공사": "건설업",
    "시설관리·서비스업": "시설관리·사업지원 서비스업",
}

// 업종 미설정 → 건설업(주 고객층), 목록에 없는 값 → 기타(범용 시나리오)
export function getTutorialSample(industry?: string | null): TutorialSample {
    const find = (name: string) => TUTORIAL_SAMPLES.find((s) => s.industry === name)
    if (!industry) return find("건설업") ?? TUTORIAL_SAMPLES[0]
    const name = LEGACY_ALIAS[industry] ?? industry
    return find(name) ?? find("기타") ?? TUTORIAL_SAMPLES[0]
}
